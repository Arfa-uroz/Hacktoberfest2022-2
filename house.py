#house using turtle in python

import turtle

#outer house
turtle.penup()
turtle.setpos(-300,0)
turtle.pendown()
turtle.right(90)
turtle.forward(300)
turtle.left(90)
turtle.forward(600)
turtle.left(90)
turtle.forward(300)
turtle.left(45)
turtle.forward(424.264)
turtle.left(90)
turtle.forward(424.264)
turtle.left(135)
turtle.forward(600)

#windows
turtle.penup()
turtle.right(90)

turtle.done()
